package br.com.ToolsChallenge.entidade;

public class Cartao {
	
	private String numero;
	
	public Cartao(String numero) {
		super();
		this.numero = numero;
	}

	public Cartao() {
		super();
	}

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}
	
	
}
